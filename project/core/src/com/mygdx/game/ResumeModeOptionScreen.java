package com.mygdx.game;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.Screen;
import com.badlogic.gdx.graphics.GL20;
import com.badlogic.gdx.graphics.Texture;

public class ResumeModeOptionScreen implements Screen {
    TankStars game;
    Texture ResuemModeOptionPhoto;

    public ResumeModeOptionScreen(TankStars game) {
        this.game = game;
        ResuemModeOptionPhoto=new Texture("ResumeModeOptionPhoto.png");
    }

    @Override
    public void render(float delta) {
        Gdx.gl.glClearColor(0,0,0,1);
        Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT);

        game.batch.begin();
        game.batch.draw(ResuemModeOptionPhoto,0,0);

        game.batch.end();
    }
    @Override
    public void show() {

    }

    @Override
    public void resize(int width, int height) {

    }

    @Override
    public void pause() {

    }

    @Override
    public void resume() {

    }

    @Override
    public void hide() {

    }

    @Override
    public void dispose() {

    }
}
